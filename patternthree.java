public class patternthree {
    public static void main(String   args[])
    {
        // nested loop
         int n = 4;
         int m = 5;
       
           // out loop
           for(int i=1; i<=n; i++)
           {
            // inner loop
            for(int j=1; j<=m; j++){
                System.out.print("*");

            }
            System.out.println();
           }

        }
    }
    

